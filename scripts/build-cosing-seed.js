#!/usr/bin/env node
// One-time script to extract COSING_DB + COSING_EXTRA from index.html
// and write server/cosing-seed.js
const fs = require('fs');
const path = require('path');
const vm = require('vm');

const html = fs.readFileSync(path.join(__dirname, '../public/index.html'), 'utf8');

// Extract the two data blocks
const dbMatch = html.match(/const COSING_DB\s*=\s*(\[[\s\S]*?\]);/);
const extraMatch = html.match(/const COSING_EXTRA\s*=\s*(\{[\s\S]*?\n\};)/);

if (!dbMatch || !extraMatch) {
  console.error('Could not find COSING_DB or COSING_EXTRA in index.html');
  process.exit(1);
}

const ctx = { COSING_DB: undefined, COSING_EXTRA: undefined };
vm.runInNewContext(
  `var COSING_DB = ${dbMatch[1]}; var COSING_EXTRA = ${extraMatch[1]};`,
  ctx
);
// vm assigns vars to the sandbox object
const COSING_DB = vm.runInNewContext(dbMatch[1]);
// The regex captures trailing semicolon — strip it for wrapping
const extraStr = extraMatch[1].replace(/;\s*$/, '');
const COSING_EXTRA = vm.runInNewContext(`(${extraStr})`);

const merged = COSING_DB.map(r => {
  const ex = COSING_EXTRA[r.inci] || {};
  return {
    i: r.inci,
    c: r.cas  || null,
    e: r.ec   || null,
    f: r.fn   || null,
    a: r.annex || null,
    m: r.max  || null,
    o: ex.origin    || null,
    u: ex.uv_range  || null,
    s: ex.sccs      || null,
    r: ex.ref       || null,
  };
});

const lines = merged.map(e => '  ' + JSON.stringify(e));
const output = `// CosIng EU offline seed data — ${merged.length} ingredients
// Source: European Commission CosIng Database (ec.europa.eu)
// Auto-generated by scripts/build-cosing-seed.js
module.exports = [
${lines.join(',\n')}
];
`;

const outPath = path.join(__dirname, '../server/cosing-seed.js');
fs.writeFileSync(outPath, output, 'utf8');
console.log(`✅ Written ${merged.length} ingredients to ${outPath}`);
